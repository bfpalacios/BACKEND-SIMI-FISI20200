			FROM tpnota nota
			LEFT JOIN tpmatricula mat ON nota.FK_ID_MATRICULA = mat.ID_MATRICULA
            LEFT JOIN tpprog_curso pcu ON mat.FK_ID_PROGCURSO = pcu.ID_PROGCURSO
